//Valores

package main

import "fmt"

func main() {
	//Manejo de Cadenas
	fmt.Println("Go" + "lang")
	//Manejo de Enteros
	fmt.Println("10+10=", 10+10)
	//Manejo de Flotantes
	fmt.Println("7.0/3.0=", 7.0/3.0)
	//Manejo de Booleanos
	fmt.Println("Resultados:")
	fmt.Println(true && false)
	//Or se escribe con Alt+124
	fmt.Println(true || false)
	//El signo de admiracion significa diferente
	fmt.Println(!true)
	fmt.Println(!false)
}
